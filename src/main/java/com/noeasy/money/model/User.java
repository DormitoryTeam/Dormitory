package com.noeasy.money.model;

public class User {

    private String mId;

    private String mName;



    /**
     * @return the id
     */
    public String getId() {
        return mId;
    }



    /**
     * @param pId
     *            the id to set
     */
    public void setId(String pId) {
        mId = pId;
    }



    /**
     * @return the name
     */
    public String getName() {
        return mName;
    }



    /**
     * @param pName
     *            the name to set
     */
    public void setName(String pName) {
        mName = pName;
    }

}
